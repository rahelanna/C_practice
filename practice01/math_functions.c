#include <stdio.h>
#include <math.h>


int main() {
    
    double A = sqrt(9);
    double B = pow(2, 4);
    int C = round(3.14);
    int D = ceil(3.14);
    int E = floor(3.99);
    double F = fabs(-100);
    double G = log(3);
    double H = sin(45);
    double I = cos(45);
    double J = tan(45);
    
    printf("\nA %lf", A);
    printf("\nB %lf", B);
    printf("\nC %d", C);
    printf("\nD %d", D);
    printf("\nE %d", E);
    printf("\nF %lf", F);
    printf("\nG %lf", G);
    printf("\nH %lf", H);
    printf("\nI %lf", I);
    printf("\nJ %lf", J);
    
    return 0;
}  